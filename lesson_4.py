## ана строка. Вывести все слова, начинающиеся на букву "а" и слова оканчивающиеся на букву "я". Например: Абстракция, авария, аллея.

s = 'а-ля абажур аббревиатура абгемахт аберрация абзац'
s = s.lower()
s = s.split(' ')
l  = [i for i in s]

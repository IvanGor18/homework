## Дана строка. Подсчитать самую длинную последовательность подряд идущих букв «н». Заменить их на восклицательные знаки, вывести новую строку, и количество подряд идущих букв "н".


s = "092348гшукрашгукрагшыванннннтдодиуцаолнно щцруатшщрцуаннннннннннннн"

k = 1 
maxx = 0 
for i in range(len(s)-1): 
    if s[i] == s[i+1]:
        k += 1
    if s[i] != s[i+1]:
        maxx = max(maxx,k)
        k = 1
print(maxx)




